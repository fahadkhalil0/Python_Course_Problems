# Class in python (OOP)
class Employee:
    name = "Fahad"
    lang = "Py"
    salary = 25000


Fahad = Employee()
Fahad.name = "Ali"
print(Fahad.name, Fahad.lang, Fahad.salary)

Fahad2 = Employee()
print(Fahad2.name, Fahad2.lang, Fahad2.salary)

Fahad3 = Employee()
print(Fahad3.name, Fahad3.lang, Fahad3.salary)

#here name is the attribute of instance and the other attributes are belong to the class.